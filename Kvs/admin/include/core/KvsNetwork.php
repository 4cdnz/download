	/**
	 * Downloads remote file.
	 *
	 * @param string $url
	 * @param string $path
	 * @param string $referer
	 * @param int $timeout
	 *
	 * @return bool
	 */
	public static function download_file(string $url, string $path, string $referer = '', int $timeout = 0): bool
	{
		save_file_from_url($url, $path, $referer, $timeout);
		if (is_file($path) && filesize($path) > 0)
		{
			KvsContext::log_debug('Downloaded file', "$url -> $path");
			return true;
		}
		return false;
	}

